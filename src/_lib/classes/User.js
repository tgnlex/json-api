class User {
  avatar = undefined;
  bio = undefined;
  website = undefined;
  constructor() {
    super()
    
    email = this.email;
    username = this.username;
    password = this.password;
  }
setAvatar(image) {this.avatar = image}
setBio(text) {this.bio = text}
setWebsite(url) {this.website = url}
changePassword(pass) {this.password = pass}
}

